package ro.msg.cm.types;

/**
 * An enum that holds all the possible individual duplications between candidates
 * ON_NAME represents that the candidates have the same first name and last name
 * ON_EMAIL represents that the candidates have the same email
 * ON_PHONE represents that the candidates have the same phone number
 */
public enum DuplicateType {
    ON_NAME,ON_EMAIL,ON_PHONE
}
